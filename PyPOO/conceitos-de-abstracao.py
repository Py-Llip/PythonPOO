"""São classes projetadas, especialmente, para servirem como modelo para classes que chamamos de derivadas.
Classes abstratas não permitem instanciar objetos.
Métodos abstratos são métodos sem corpo, incompletos.
Para que uma classe seja abstrata é necessário que apenas um método seja abstratos.
Se uma classe possui uma método abstrato, as classes imediatamente descendentes deverão implementá-lo.
Em python, o módulo abc (Abstract base Classes) fornece uma infraestrutura para a definição de classes base abstratas."""

